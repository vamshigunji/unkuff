export function getCountryByCode(code: string): Country | undefined {
    return COUNTRIES.find(c => c.code === code);
}

export function searchCountries(query: string): Country[] {
    const lowerQuery = query.toLowerCase();
    return COUNTRIES.filter(c =>
        c.name.toLowerCase().includes(lowerQuery) ||
        c.code.toLowerCase().includes(lowerQuery)
    );
}

export function getCitySuggestions(countryCode: string, query: string): string[] {
    const cities = CITY_SUGGESTIONS[countryCode] || [];
    if (!query) return cities.slice(0, 10); // Show first 10 if no query
    const lowerQuery = query.toLowerCase();
    return cities.filter(city => city.toLowerCase().includes(lowerQuery)).slice(0, 10);
}
